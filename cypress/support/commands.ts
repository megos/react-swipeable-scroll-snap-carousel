// -- This is a parent command --
Cypress.Commands.add('visible', (title: number) => {
  ;[...new Array(4)].forEach((_, index) => {
    const currentTitle = index + 1
    cy.contains('div', currentTitle).should(
      title === currentTitle ? 'be.visible' : 'not.contain',
    )
  })
})
